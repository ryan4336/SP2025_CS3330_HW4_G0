package hw4.player;
/**
 * Defines the possible movement directions in the game.
 * These directions represent the ways the player can navigate through the maze.
 */
public enum Movement {
	/**
     * Represents moving up in the grid.
     */
	UP, 
	/**
     * Represents moving right in the grid.
     */
	RIGHT, 
	/**
     * Represents moving down in the grid.
     */
	DOWN, 
	/**
     * Represents moving left in the grid.
     */
	LEFT

}
